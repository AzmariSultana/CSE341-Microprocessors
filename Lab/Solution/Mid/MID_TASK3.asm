.DATA

;declare variables here
UNIT DW 0
BILL DW 0
SUR DW 0

MSG1 DB 13,10,'Minimum bill has to be paid$'
MSG2 DB 13,10,'Payable bill: Tk. $'

.CODE
MAIN PROC
    
;initialize DS

MOV AX, @DATA
MOV DS, AX

;enter you code here

;INPUT
MOV AH,1
INT 21H
SUB AL,48
MOV BL,100
MUL BL
MOV UNIT,AX

MOV AH,1
INT 21H
SUB AL,48
MOV BL,10
MUL BL
ADD UNIT,AX

MOV AH,1
INT 21H
SUB AL,48
MOV AH,0
ADD UNIT,AX

;CONDITION CHECK
MOV AX, UNIT
CMP AX, 0
JL EXIT
CMP AX,199
JLE CON1
CMP AX,399
JLE CON2
CMP AX,599
JLE CON3
JMP CON4

CON1:
MOV BL,1
JMP CALC
CON2:
MOV BL,2
JMP CALC
CON3:
MOV BL,3
JMP CALC
CON4:
MOV BL, 4

CALC:
MOV AX, UNIT
MUL BX
MOV BILL, AX

;MINIMUM BILL
CMP BILL,120
JGE CALC_SURCHARGE
LEA DX, MSG1
MOV AH,9
INT 21H
MOV BILL,120
JMP PRINT

;SURCHARGE
CALC_SURCHARGE:
CMP BILL,500
JLE PRINT
MOV AX,BILL
MOV BX,20
MUL BX
MOV BX,100
DIV BX
ADD BILL,AX

;PRINT BILL
PRINT:
LEA DX, MSG2
MOV AH,9
INT 21H

MOV DX, 0
MOV AX, BILL

MOV BX,1000
DIV BX
MOV CX, DX
ADD AL,48
MOV DL,AL
MOV AH,2
INT 21H

MOV DX, 0
MOV AX,CX
MOV BX,100
DIV BX
MOV CX, DX
ADD AL,48
MOV DL,AL
MOV AH,2
INT 21H

MOV AX,CX
MOV BL,10
DIV BL
MOV CH, AH
ADD AL,48
MOV DL,AL
MOV AH,2
INT 21H


ADD CH,48
MOV DL,CH
MOV AH,2
INT 21H

;exit to DOS
EXIT:
MOV AX,4C00H
INT 21H 

MAIN ENDP
 END MAIN