.MODEL SMALL
 
.STACK 100H

.DATA

; declare variables here
STR1 DW "SATURDAY$" 
STR2 DW "SUNDAY$"
STR3 DW "MONDAY$"
STR4 DW "TUESDAY$"
STR5 DW "WEDNESDAY$"
STR6 DW "THIRSDAY$"
STR7 DW "FRIDAY$"

.CODE
MAIN PROC

; initialize DS

MOV AX,@DATA
MOV DS,AX
 
; enter your code here
;Write a case to print the name of the day of the week. Consider the first day of the week
;is Saturday.

MOV AH,1
INT 21H
SUB AL,48
MOV DL,AL

CMP DL,1
JE PRINT1
JG CON1

PRINT1:
LEA DX, STR1
MOV AH,9
INT 21H
JMP EXIT

CON1:
CMP DL,2
JE PRINT2
JG CON2

PRINT2:
LEA DX, STR2
MOV AH,9
INT 21H
JMP EXIT

CON2:
CMP DL,3
JE PRINT3
JG CON3

PRINT3:
LEA DX, STR3
MOV AH,9
INT 21H
JMP EXIT 

CON3:
CMP DL,4
JE PRINT4
JG CON4

PRINT4:
LEA DX, STR4
MOV AH,9
INT 21H
JMP EXIT

CON4:
CMP DL,5
JE PRINT5
JG CON5

PRINT5:
LEA DX, STR5
MOV AH,9
INT 21H
JMP EXIT

CON5:
CMP DL,6
JE PRINT6
JG CON6

PRINT6:
LEA DX, STR6
MOV AH,9
INT 21H
JMP EXIT

CON6:
CMP DL,7
JE PRINT7

PRINT7:
LEA DX, STR7
MOV AH,9
INT 21H


;exit to DOS
EXIT:
               
MOV AX,4C00H
INT 21H

MAIN ENDP
    END MAIN
