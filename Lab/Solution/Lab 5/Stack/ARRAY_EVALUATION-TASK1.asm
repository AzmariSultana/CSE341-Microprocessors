.MODEL SMALL
 
.STACK 100H

.DATA

; declare variables here

SIGNAL_DATA DB 45,62,55,78,30

.CODE
MAIN PROC

; initialize DS

MOV AX,@DATA
MOV DS,AX
 
; enter your code here
;EVALUATION 1

MOV CX,5
MOV SI,0

MOV DL,0

LOOP_START: 
MOV AL,SIGNAL_DATA[SI] 
INC SI 
MOV BL,60
CMP AL,BL
JL COUNT
LOOP LOOP_START
JMP OUTPUT

COUNT: 
INC DL
LOOP LOOP_START 

OUTPUT:
ADD DL,30H
MOV AH,2
INT 21H  

;exit to DOS
EXIT:               
MOV AX,4C00H
INT 21H

MAIN ENDP
    END MAIN
